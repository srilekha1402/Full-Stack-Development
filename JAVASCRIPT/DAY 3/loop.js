// WHILE Loop

let i=1;
console.log("WHILE LOOP START");
while(i<=5){
    console.log(i);
    i++;    
}
console.log("WHILE LOOP END");
console.log("            ");
//DO WHILE LOOP
console.log(" DO WHILE LOOP START");
let a=5
do{
    console.log(a);
    a--;
}while(a>=1)
console.log("DO WHILE LOOP END");
console.log("            ");
console.log("FOR LOOP START");
for(let i=1;i<=5;i++){
    console.log(i);
    
}
console.log("FOR LOOP END");
